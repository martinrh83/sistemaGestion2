    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed

        try {
            String nombre=txtnombre.getText();
            String apellido=txtapellido.getText();
            int cod= Integer.parseInt(txtdni.getText());
            if(Biblioteca.buscarCliente(cod)==-1){
                Cliente cliente =new Cliente(nombre, apellido, cod);
              Biblioteca.getListacliente().add(cliente);
                JOptionPane.showMessageDialog(this, "Cliente Agregado");
                System.out.println(cliente);
                limpiar();
                inhabilitar();
                btnnuevo.setEnabled(true);
                visualizar();} else {
                JOptionPane.showMessageDialog(this, "CLIENTE YA INGRESADO");
                }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);

        }
    }//GEN-LAST:event_btnaceptarActionPerformed

    private void btncancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncancelActionPerformed
        try {
            String nombre=txtnombre.getText();
            String apellido=txtapellido.getText();
            int cod= Integer.parseInt(txtdni.getText());
            int dni=Biblioteca.buscarCliente(cod);
            int asd=Biblioteca.getListacliente().get(posi_cliente).getDni();
            if(dni==-1||cod==asd ){

              Biblioteca.getListacliente().get(posi_cliente).setApellido(apellido);
               Biblioteca.getListacliente().get(posi_cliente).setNombre(nombre);
                Biblioteca.getListacliente().get(posi_cliente).setDni(cod);
                JOptionPane.showMessageDialog(this, "Cliente Modificado");

                limpiar();
                inhabilitar();
                btnnuevo.setEnabled(true);
                visualizar();}
 else
            {
                JOptionPane.showMessageDialog(this, "Cliente ya  registrado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
            
}//GEN-LAST:event_btncancelActionPerformed

