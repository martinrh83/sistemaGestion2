    private void visualizar() {
       datos = new DefaultTableModel();
        datos.addColumn("Nombre");
        datos.addColumn("Apellido");
        datos.addColumn("DNI");


        for (int i=0; i<Biblioteca.getListacliente().size();i++)
            {
            Object[] fila = new Object[]{
                Biblioteca.getListacliente().get(i).getNombre(),
                 Biblioteca.getListacliente().get(i).getApellido(),
                Biblioteca.getListacliente().get(i).getDni()

            };
            datos.addRow(fila);
        }
        tblgrilla.setModel(datos);
}
